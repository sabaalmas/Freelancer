package StudentRecords;
public class StudentRecord {

    String name;
    String education;
    public void setEducation(String education) {
        this.education = education;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getEducation() {
        return education;
    }
    public String getName(){
        return name;
    }
}
